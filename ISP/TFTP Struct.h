#pragma once
#include <vector>
using namespace std;




enum TFTP_REQTYPE
{
	TR_READ = 1,
	TR_WRITE = 2,
};



// 数据包
struct TFTP_DATA_PACK
{
	WORD		m_wOPCode;		// 操作码
	WORD		m_wBlkNum;		// 块编号
	BYTE			m_szData[512];	// 数据内容
	TFTP_DATA_PACK()
	{
		m_wOPCode = 3;
		ZeroMemory(m_szData, 512);
	}
};



// ACK
struct TFTP_ACK
{
	WORD		m_wOPCode;		// 操作码
	WORD		m_wBlkNum;		// 块编号	
	TFTP_ACK()
	{
		m_wOPCode = 4;
	}
};




// ERROR
struct TFTP_ERROR
{
	WORD		m_wOPCode;		// 操作码
	WORD		m_wErrCode;		// 差错码	
	CString		m_strErrorInfo;	// 错误信息
	TFTP_ERROR()
	{
		m_wOPCode = 5;
	}
};

const int c_nDataLen=256;

struct TFTP_ItemPair
{
	BYTE			m_strItem[256];
	BYTE			m_strValue[256];

};

// WR Request
struct TFTP_WRRequest
{
	WORD		m_wOPCode;							// 操作码
	BYTE			m_strFileName[256];					// 文件名
	BYTE			m_strMode[256];						// 模式
	vector<TFTP_ItemPair*>		m_szItems;		// 选项
	TFTP_WRRequest()
	{
		m_wOPCode = 1;
	}
};


// OACK
struct TFTP_OACK
{
	WORD		m_wOPCode;				// 操作码
	CString		m_strFileName;			// 文件名
	CString		m_strMode;					// 模式
	vector<TFTP_ItemPair*>		m_szItem;		// 选项/值对	
	TFTP_OACK()
	{
		m_wOPCode = 6;
	};
};




